// main function
fun main() {
    val openOffice = 7
    val now = 8
    val office = if (now > openOffice) "Office already open" else "office close"
    // if berperan sebagai expression karena dapat memberi/menyimpan nilai kepada office
    println(office)
}

/*
Expression adalah Operator, Variabel,
dan sesuatu yang dapat memberi nilai ke dalam
suatu nilai tunggal atau variabel
 */